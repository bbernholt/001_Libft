#include <unistd.h>
#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*ptr;

	if (!lst)
		return (NULL);
	else
		ptr = lst;
	while (lst->next != NULL)
	{
		lst = lst->next;
		ptr = lst;
	}
	return (ptr);
}
